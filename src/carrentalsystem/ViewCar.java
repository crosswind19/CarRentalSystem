    private void get_all_car_detailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_get_all_car_detailsActionPerformed
        File read_car_details = new File("Car.txt");
        try {
            BufferedReader read_car = new BufferedReader(new FileReader(read_car_details));
            String each_car_line = read_car.readLine();
            //System.out.println(each_car_line);
            //display in table form (car details)
            DefaultTableModel show_car_details = (DefaultTableModel)car_details_table.getModel();
            
            Object[] car_info = read_car.lines().toArray();


            for(int i=0; i<car_info.length; i++){
                String car_line = car_info[i].toString();
                //System.out.println(car_line);
                String get_all_data[] = car_line.split("\t");

                
                    show_car_details.addRow(get_all_data);
                
                
            }
        
        get_all_car_details.setEnabled(false);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ViewCar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ViewCar.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_get_all_car_detailsActionPerformed

